let arr = ["red", "blue", "green"];
arr[0] = "pink";

/*Stack simulation methods*/

//Push Method allows you to enter at the end
arr.push("purple");
//Pop method allows you to delete the element from the end
arr.pop();

/*Queue simulation methods*/

//Shift method allows you to delete from the front
arr.shift;
//Unshift method allows you to add from the front

arr.unshift("black");
for (i = 0; i < arr.length; i++) {
  console.log(arr[i]);
}
